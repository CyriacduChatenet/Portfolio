import { useEffect, useRef } from "react";
import gsap from "gsap";

import BottomPageControl from "@/components/bottomPageControl";
import Header from "@/components/header";
import Layout from "@/components/layout";
import TalksAboutSection from "@/components/talks/about";
import useMenu from "@/contexts/useMenu";
import useAnimateMenu from "@/hooks/animations/useAnimateMenu";
import { useBodyScroll, useBodyScrollLock } from "@/hooks/useBodyScroll";

import styles from "@/pages/talks/style.module.scss";


const TalksPage = () => {
    const menuBackgroundRef = useRef(null);
    const titleRef = useRef(null);

    const { isOpen } = useMenu();

    useAnimateMenu(isOpen, menuBackgroundRef);
    useBodyScroll(isOpen);
    useBodyScrollLock(isOpen);

    const handleAnimate = () => {
        gsap.fromTo(titleRef.current, { opacity:0 }, { opacity:100, duration: 2.5, ease: "circ.inOut", delay:1 });
    };

    if(isOpen === false) {
        handleAnimate();
    }

    useEffect(() => {
        handleAnimate();
    }, [])

  return (
    <>
      <header className={styles.header}ref={menuBackgroundRef}></header>
      <Layout
        title={"Create Next App"}
        description={"Generated by create next app"}
      >
        <div className={styles.container}>
          <Header
            title={"Talks"}
            description={"My different talks in developer conference"}
          />
          <div className={styles.content}>
            <div className={styles.line}></div>
            <h2 className={styles.title} ref={titleRef}>Tech Ctrl</h2>
            <div className={styles.videoContainer}>
              <video controls className={styles.videoPlayer}>
                <source
                  src="https://www.youtube.com/watch?v=r_JTTswNdIs"
                  type="video/mp4"
                />
              </video>
            </div>
            <TalksAboutSection />
            <div className={styles.line}></div>
            <BottomPageControl link={"/contact"} label={"Contact"} />
          </div>
        </div>
      </Layout>
    </>
  );
};

export default TalksPage;
